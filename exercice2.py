
def add_fract(fr1, fr2) :
    a, b = fr1
    d, c = fr2
    return a * c + b * d, b * c

def sous_fract(fr1, fr2) :
    a, b = fr1
    d, c = fr2
    return a * c - d * b, b * c

def mult_fract(fr1, fr2) :
    a, b = fr1
    d, c = fr2
    return a * d, b * c

def div_fract(fr1, fr2) :
    a, b = fr1
    d, c = fr2
    return a * c / b * d

 





